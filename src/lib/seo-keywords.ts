function unique(values: string[]) {
  return Array.from(new Set(values));
}

function withLocationVariants(keyword: string) {
  return locationVariants.flatMap((location) => [
    `${keyword} ${location}`,
    `${keyword} في ${location}`,
    `${keyword} فى ${location}`,
    `${keyword} ب${location}`,
  ]);
}

export const seoKeywords = unique([
  ...serviceKeywords,
  ...brandKeywords,
  ...locationVariants,
  ...serviceKeywords.flatMap(withLocationVariants),
]);
